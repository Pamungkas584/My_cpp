#include <iostream>
using namespace std;

int main() {

    // increcement dan decrecement
    // preincrement dan postincrement
    
    int a = 5;
    int b = 5;

    // postincrement 
    cout << a << endl;
    a++; // ketika di print maka nilai yang di tambilkan adalah nilai sebelum di tambah satu
    cout << a << endl;

    // preincrement
    cout << b << endl;
    ++b; // ketika di print maka nilai yang di tampilkan adalah nilai sesudah di tambah satu
    cout << b << endl;
    



    cin.get();
    return 0;
}